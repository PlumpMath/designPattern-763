package com.zhangqi.duck.shape;

public enum Shape {
	HEAD("head", "Í·");

	public String code;
	public String desc;

	private Shape(String code, String desc) {
		this.code = code;
		this.desc = desc;
	}

	public String getCode() {
		return code;
	}

	public void setCode(String code) {
		this.code = code;
	}

	public String getDesc() {
		return desc;
	}

	public void setDesc(String desc) {
		this.desc = desc;
	}

}
